package Module_1.basic_1.Chapter2_Operator.chapter2_control_flows;

public class Ifstatement {
    public static void main(String[] args) {

        //if statement;
        //while statement;
        //do statement;


        // syntax for if statement;
        // if (condition) {implementation};
        // for i // for r // for;
        int a = 6;
        if (a > 6) {
            System.out.println(a);
        } else {
            System.out.println("This is not your acc");
        }

        int day = 4;
        if (day == 1) {
            System.out.println("This is day one");
        } else if (day == 2) {
            System.out.println("This is day two");
        } else if (day == 3) {
            System.out.println("This is day three");
        } else {
            System.out.println("This is day four");
        }

        // while statement;
        // while (condition){implementation};
        int b = 0;
        while (b <= 5) {
            System.out.println(b);
            b++;
        }

        // do statement;
        // do {implementation} while (condition);
        do {
            System.out.println(a);
            a++;
        }  while (a < 9) ;
        }
    }



